package Bean.Prof;

public class ProfBean {

    int proid;
    String proname,propas,prodes;

    public int getProid() {
        return proid;
    }

    public void setProid(int proid) {
        this.proid = proid;
    }

    public String getProname() {
        return proname;
    }

    public void setProname(String proname) {
        this.proname = proname;
    }

    public String getPropas() {
        return propas;
    }

    public void setPropas(String propas) {
        this.propas = propas;
    }

    public String getProdes() {
        return prodes;
    }

    public void setProdes(String prodes) {
        this.prodes = prodes;
    }
}
